# Clase de operadores

# Aritméticos:

# + - * / %

numero1 = int(input("Ingresa un numero (1): "))
numero2 = int(input("Ingresa un numero (2): "))

# Imprimir resultado:

# Suma:
print(f"La suma es: {numero1 + numero2}")
# Resta:
print(f"La resta es: {numero1 - numero2}")
# Multiplicación:
print(f"La multiplicación es: {numero1 * numero2}")
# División:
print(f"La división es: {numero1 / numero2}")
# Módulo:
print(f"El módulo es: {numero1 // numero2}")






